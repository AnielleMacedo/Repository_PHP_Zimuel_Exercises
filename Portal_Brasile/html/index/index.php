        <p>Noronha, come la chiamano i locali con affetto, è un paradiso per il surf, le immersioni, per il mare, le spiagge e per le escursioni.

        In totale l'arcipelago di Fernando de Noronha è composto da 21 isole ed isolotti e da una miriade di piscine naturali abitate da pesci e coralli. L'isola principale, Fernando de Noronha, dà il nome all'arcipelago.</p> 

        <p>Le isole sono distanti circa 350 km dalla terra ferma e per raggiungerle ci vogliono alcuni giorni di navigazione. 

        Sono le parti emerse di antiche catene montuose sottomarina, tutte di origine vulcanica. L'isola di Noronha misura 18 chilometri quadrati e da sola forma il 91% del territorio dell'arcipelago. Le altre isole sono Rata, Sela Gineta, Cabeluda e São Jose, oltre alle due isolette minuscole di Leão e Viúva.</p>

        <p>La principale attrazione dell'isola è la sua fauna marina. Si possono vedere facilmente tartarughe marine, albatros e delfini e fare delle meravigliose immersioni. Oltre i 40 metri di profondità infatti ci sono acque calde ed una fauna colorata.

        Nella sua travagliata storia, dalla scoperta nel 1500, l'arcipelago di Fernando de Noronha passò per diverse mani fino a che i portoghesi non si decisero a fortificarlo. Sono del 700 le fortificazioni sull'isola principale, collegate dalle strade. L'unico centro abitato dell'arcipelago è Vila Dos Remédios costruita nel 1770. </p>

        <p style="font-weight: bolder">SPIAGGE DI FERNANDO DE NORONHA</p>
        <p>Le spiagge a Fernando de Noronha sono meravigliose. Profonde insenature protette dalle rocce verdi ed aguzze delle isole garantiscono acqua calma e verde smeraldo. Le correnti assicurano invece una temperatura del mare ideale per fare il bagno durante tutto l'anno.

        Nell'isola principale ci sono Praia do Sancho, Baía dos Porcos e Cacimba do Padre che si affacciano sul Mare de Dentro (quello rivolto veso le coste brasiliane). Sul Mar de Fora ci sono meno spiagge ma su questo lato dell'isola, specialmente al sud, si fa il miglior snorkeling dell'arcipelago (Praia do Porto e Atalaia). Praia do Leão è il posto dove vedere le tartarughe marine, mentre il punto d'osservazione migliore per vedere i delfini è Mirante dos Golfinhos. </p>

        <p>Il miglior surf si fa a Cacimba do Padre, mentre a Perto da Vila dos Remédios ci sono Praia do Cachorro, dove c'è anche un bar, Praia do Meio e Praia da Conceição.

        Praia dos Porcos e praia do Sancho (forse la più bella dell'arcipelago) si trovano sullo stesso lato ed è facile visitarle entrambe.</p>
